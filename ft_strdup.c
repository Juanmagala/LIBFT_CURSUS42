#include "libft.h"

char	*ft_strdup(const char *s)
{
	unsigned int	i;
	char			*dest;

	i = 0;
	dest = (char *)malloc(ft_strlen(s) + 1);
	if (dest == 0)
		return (0);
	while (s[i] != '\0')
	{
		dest[i] = s[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
